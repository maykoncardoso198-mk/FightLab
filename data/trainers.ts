import { Trainer, DaySchedule } from './types';

const generateSchedule = (): DaySchedule[] => {
  const weekdays = ['SEG', 'TER', 'QUA', 'QUI', 'SEX', 'SAB', 'DOM'];
  const today = new Date();
  return weekdays.map((wd, i) => {
    const date = new Date(today);
    date.setDate(today.getDate() + i);
    return {
      date: date.toISOString().split('T')[0],
      weekday: wd,
      slots: [
        { time: '06:00', available: Math.random() > 0.3 },
        { time: '07:00', available: Math.random() > 0.4 },
        { time: '09:00', available: Math.random() > 0.3 },
        { time: '11:00', available: Math.random() > 0.4 },
        { time: '14:00', available: Math.random() > 0.3 },
        { time: '16:00', available: Math.random() > 0.5 },
        { time: '18:00', available: Math.random() > 0.3 },
        { time: '19:00', available: Math.random() > 0.4 },
        { time: '20:00', available: Math.random() > 0.3 },
      ],
    };
  });
};

